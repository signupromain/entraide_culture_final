                        <h2>n.t.c.i</h2>
                        <p><br>
                            Depuis 2014, Entraide et Culture propose un soutien numérique à son public sous la forme d’initiations et de formations en informatique et aux technologies de l’information de la communication (TIC).
                            <br>
                            Nous accueillons principalement un public de quartier, fragilisé au niveau de l’emploi, avec de faibles revenus, disposant de peu de matériel et de peu de ressources en informatique.
