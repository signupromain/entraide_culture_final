                        <h2>Aide à domicile</h2>
                        <p><br>
                            Le service d’Aide à domicile propose deux types d’aides : aides ménagères ou aides techniques. Il s’adresse principalement aux personnes en situation de précarité qui ne peuvent plus assumer seules, physiquement et/ou psychologiquement, les tâches quotidiennes tant au niveau de l’entretien de leur domicile qu’au niveau des travaux à y effectuer.
                            <br>
                            <br>
                            Les aides ménagères proposent l’entretien régulier de l’habitation des personnes âgées, à mobilité réduite ou handicapées, vivant en dessous du seuil de pauvreté et ce prioritairement aux habitants de Forest et des communes avoisinantes.
                            <br>
                            Grâce à un entretien social préalable, nous définissons les conditions de l’aide.
                            <br>
                            Une prestation pour les tâches ménagères dure au minimum 3 heures et est consacrée essentiellement à maintenir le logement en ordre, à veiller à l’hygiène, à garantir un environnement propice au bien-être de l’usager, à lessiver et à repasser.
                            <br>
                            L’usager qui bénéficie de ces services s’engage à respecter la charte du travail des aides ménagères. Elle vise à informer les bénéficiaires et les aides ménagères de leurs droits et obligations respectifs et à prévenir tout risque de discrimination.
                            <br>
                            <br>
                            Les aides techniques comprennent différents types de travaux de rénovation, de peinture, de tapissage, de bricolage, de lavage de vitres et de dépannage divers. Elles s’adressent aux personnes dans l’incapacité d’effectuer ces travaux, soit physiquement, soit techniquement, soit financièrement. D’expérience, nous constatons que le public touché par ce type d’aide technique est surtout composé de mères célibataires, de couples surendettés, de personnes âgées, handicapées et de toutes personnes en difficultés.
