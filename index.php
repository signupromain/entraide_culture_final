                        <h2>aide alimentaire</h2>
                        <p><br>
                            Le service d’aide alimentaire distribue des colis alimentaires aux personnes les plus démunies plusieurs fois par semaine en fonction de la situation sociale préalablement évaluée. Il met en place différents partenariats locaux autour de l’aide alimentaire.
                            <br>
                            Sur présentation d’une attestation délivrée au bénéficiaire par le CPAS de Forest, nous distribuons des colis de l’Union Européenne. Sous certaines conditions, nous distribuons également des colis alimentaires aux personnes vivant en situation de précarité.
                            <br>
                            Notre service social examine les demandes d’aide alimentaire et octroie selon certaines conditions une aide soit régulière, soit urgente, soit temporaire.
                            <br>
                            <br>
                            Les <strong>colis réguliers</strong> sont distribués chaque semaine aux personnes ayant des ressources trop faibles pour vivre décemment. Les colis alimentaires sont constitués en fonction des produits disponibles et du nombre de personnes que comprend le ménage. L’aide alimentaire régulière concerne les personnes qui vivent sous le seuil de pauvreté. Afin de calculer le revenu de la personne ou du ménage, on tiendra compte des ressources réellement disponibles en décomptant les dettes ou les loyers très élevés.
                            <br>
                            <br>
                            Les <strong>colis d’urgence</strong> sont distribués aux personnes en difficultés qui ont besoin d’une aide sociale rapide. Ces personnes sont souvent orientées chez nous par les CPAS ou les relais sociaux. L’aide alimentaire urgente et temporaire concerne trois catégories de bénéficiaires : les personnes bénéficiant d’un revenu d’intégration sociale (une aide régulière sera liée à la présentation d’un document délivré par le CPAS de Forest), les personnes sans-abris ou les personnes en séjour illégal.
                            <br>
                            <br>
                            Les <strong>colis temporaires</strong> sont distribués toutes les semaines pour une durée limitée aux personnes dont la situation sociale est difficile et ce jusqu’à stabilisation de leur situation.
