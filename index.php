                            <br>
                            - initiation à Internet : création et usage courant d’une adresse mail, utilisation d’un moteur de recherche … ;
                            <br>
                            - découverte d'outils pratiques en ligne, comment consulter son compte en banque… ;
                            <br>
                            - découverte des périphériques et des différentes plateformes informatiques ;
                            <br>
                            - développement d’une réflexion préventive : obsolescence programmée, surconsommation, impact écologique et géopolitique.
                            <br>
                            L'apprentissage est conçu de manière à favoriser l’autonomie des apprenants face aux différentes interfaces en changement perpétuel et à les amener à développer une pensée construite et critique du monde numérique.
